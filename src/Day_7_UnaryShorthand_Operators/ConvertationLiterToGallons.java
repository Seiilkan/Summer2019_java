package Day_7_UnaryShorthand_Operators;

public class ConvertationLiterToGallons {
	
public static void main(String[] args) {
	
	int gallon = 100;
	double liter = gallon * 3.785;
	System.out.println(liter); //378.5
	
	System.out.println(gallon +" gallons equal to "+liter + " liters ");
	
	int liters = 1;
	double gallons = liters / 3.785;
	System.out.println(gallons); //
	
	System.out.println(liters +" liter equal to "+gallons + " gallons ");
	
	
	
	
	
	
	
	
}	

}
